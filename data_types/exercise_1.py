# Difference between data type and class? data type is a more general 
# term, e.g. If we say "this is a string", that's a more universal
# kind of label, i.e. all programming languages have strings 
# as a data type. But "str", the string class, is where strings 
# are defined in Python. If you need more info about strings, 
# you look in the documentation for the "str" class.

# "literals" - things you can type on the screen to 
# instantly create an object.

# 'True'          = String/str
# False           = Boolean/bool
# (1, 2, 3)       = Tuple/tuple
# 1.5             = Float/float
# [1, 2, 3]       = List/list
# 2               = Integer/int
# range(5)        = Range/range    
# (above we're invoking range constructor function. Ranges don't have a literal syntax.)
# {1, 2, 3}       = Set/set
# None            = NoneType/NoneType
